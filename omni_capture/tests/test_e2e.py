def test_dedup_prevents_double_write(vault: Path):
    """Running the pipeline twice with identical text should not create a second file."""
    from main import run_pipeline

    text = "Unique content that should only be stored once in the vault."
    # "note" is a filename stopword (see storage_engine._FILENAME_STOPWORDS),
    # so the file actually lands at "dedup-check.md".
