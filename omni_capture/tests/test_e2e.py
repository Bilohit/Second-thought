    with _mock_llm(fake_out):
        r1 = run_pipeline(text=text, vault_root=str(vault), dry_run=False, notify=False)

    # Second run — same text, should be deduplicated (no second vault write)
    fake_out2 = _fake_capture_output(filename="dedup-check-note")
