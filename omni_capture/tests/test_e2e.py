"""
tests/test_e2e.py
-----------------
End-to-end test suite for Project Second Thought.

Covers every input type through the full pipeline:
  text    → enrichment → LLM (mocked) → vault write
  url     → web enrichment (mocked) → LLM (mocked) → vault write
  image   → _enrich_image / LLaVA (mocked) → LLM (mocked) → vault write
  audio   → _enrich_audio / Whisper (mocked) → LLM (mocked) → vault write
  share   → /share FastAPI endpoint → pipeline → vault write (via TestClient)

Each test asserts:
  1. The vault file is created in the correct category sub-directory.
  2. The file contains expected Markdown content.
  3. No live Ollama / Whisper / internet call is made (all stubbed).

Run from the repo root:
  pytest omni_capture/tests/test_e2e.py -v
"""
