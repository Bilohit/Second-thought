    fake_html = b"<html><body><p>Async guide.</p></body></html>"
    fake_resp = mock.MagicMock()
    fake_resp.read.return_value = fake_html
    fake_resp.__enter__ = lambda s: s
    fake_resp.__exit__ = mock.MagicMock(return_value=False)

    fake_out = _fake_capture_output(filename="share-test-note")

    import server as srv_mod
    client = TestClient(srv_mod.app, raise_server_exceptions=True)

    with _mock_llm(fake_out), \
         mock.patch("urllib.request.urlopen", return_value=fake_resp), \
         mock.patch("vector_store.retrieve_related", return_value=[]), \
         mock.patch("vector_store.index_note"):
        resp = client.post(
            "/share",
            json={"url": "https://realpython.com/async-io-python/", "title": "Async IO"},
            headers=_auth_headers(),
        )

    assert resp.status_code == 200

    # Parse the SSE stream for a "done" event
    done_data = None
    for line in resp.text.splitlines():
        if line.startswith("data:"):
            try:
                d = json.loads(line[5:])
                if "path" in d:
                    done_data = d
            except Exception:
                pass

    assert done_data is not None, f"No 'done' event in SSE stream:\n{resp.text}"
    assert Path(done_data["path"]).exists()


