# ── Helpers ────────────────────────────────────────────────────────────────────


def _written_file(vault: Path, category: str, filename: str) -> Path:
    """Locate the written Markdown file inside the vault."""
    cat_dir = vault / category
    candidates = list(cat_dir.glob(f"{filename}*.md"))
    assert candidates, (
        f"Expected a file matching '{filename}*.md' in {cat_dir}, "
        f"but found: {list(cat_dir.iterdir()) if cat_dir.exists() else '(dir missing)'}"
    )
    return candidates[0]


